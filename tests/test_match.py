def test_get_connection_info():
    match = Match(**masterblaster_match)
    expected = {}
    for k, v in masterblaster_match["connectionInfo"]["fields"].items():
        if k == "usedGameServers":
            expected[k.lower()] = []
            gss = []
            for game_server in v:
                gs = {}
                for k2, v2 in game_server.items():
                    gs[k2.lower()] = v2
                gss.append(gs)
            expected[k.lower()] = gss
        else:
            expected[k.lower()] = v
    connection_info = match.get_connection_info()
    for k, v in connection_info.__dict__.items():
        if k == "used_game_servers":
            for game_server in v:
                for k2, v2 in game_server.__dict__.items():
                    assert v2 == expected["usedgameservers"][0][k2.replace("_", "")]
        else:
            assert v == expected[k.replace("_", "")]


